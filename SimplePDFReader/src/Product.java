public class Product {
    public String name;
    public int SKU;
    public int quantity;
    public double price;

    public Product(String name, int SKU, int quantity, double price){
        this.name = name;
        this.SKU = SKU;
        this.quantity = quantity;
        this.price = price;
    }
}
